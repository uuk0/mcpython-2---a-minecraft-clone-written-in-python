import globals as G
import mathhelper

"""class for white wool"""


class WhiteWool(G.blockclass):
    def getName(self):
        return "minecraft:white_wool"

    def getTexturData(self, inst):
        return mathhelper.tex_coords((0, 0), (0, 0), (0, 0), n2=1)

    def getTexturFile(self, inst):
        return "minecraft/white_wool"

    def getAllTexturFiles(self):
        return ["minecraft/white_wool"]


G.blockhandler.register(WhiteWool)

