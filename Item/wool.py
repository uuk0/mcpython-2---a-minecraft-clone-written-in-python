import globals as G

"""class for white wool"""


class whiteWool(G.itemclass):
    def getName(self):
        return "minecraft:white_wool"

    def hasBlock(self):
        return True

    def getTexturFile(self):
        return G.local + "/assets/minecraft/textures/item/WOOL#0.png"


G.itemhandler.register(whiteWool)

